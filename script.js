    function adjustTextColor(hexColor) {
        // Convert hex to RGB
        const r = parseInt(hexColor.slice(1, 3), 16);
        const g = parseInt(hexColor.slice(3, 5), 16);
        const b = parseInt(hexColor.slice(5, 7), 16);

        // Calculate brightness (YIQ formula)
        const yiq = ((r * 299) + (g * 587) + (b * 114)) / 1000;

        // Return black or white based on brightness
        return yiq >= 128 ? 'black' : 'white';
    }

    function adjustOutlineColor(hexColor, adjustment) {
        // Convert hex to RGB
        let r = parseInt(hexColor.slice(1, 3), 16);
        let g = parseInt(hexColor.slice(3, 5), 16);
        let b = parseInt(hexColor.slice(5, 7), 16);

        // Adjust RGB values (ensure they stay within 0-255 range)
        r = Math.min(255, Math.max(0, r + adjustment));
        g = Math.min(255, Math.max(0, g + adjustment));
        b = Math.min(255, Math.max(0, b + adjustment));

        // Convert back to hex
        return `#${r.toString(16).padStart(2, '0')}${g.toString(16).padStart(2, '0')}${b.toString(16).padStart(2, '0')}`;
    }

